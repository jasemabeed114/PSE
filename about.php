			    	<h4 class="centered">What is a Credit Union?</h4>
			    	<p>
			    		Credit Unions are not-for-profit cooperative financial institutions that are owned by their members and run by volunteer board members.
			    	</p>
			    	<p>
			    		Credit unions offer better rates on loans and savings than most commercial banks, due to a credit unions' unique operation. While banks must make profits for their shareholders, credit unions exist only to serve members like you!
			    	</p>
			    	<p>
			    		Credit unions exist solely for the purpose of meeting the financial needs of their member-owners. Credit unions are known for providing outstanding personal service. Members often earn higher returns on their savings while paying lower rates for loans. Each year, credit unions consistently outshine banks in the area of consumer/member satisfaction. Their non-profit status enables credit unions to operate at a lower cost than many for-profit institutions and helps them to offer competitive loan and savings rates.
			    	</p>
